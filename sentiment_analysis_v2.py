def normalize_sentiment(label: str) -> int:
    """Map any model's label convention to +1 / 0 / -1."""
    l = label.lower()
    if l in ('positive', 'pos', 'label_2'): return  1
    if l in ('negative', 'neg', 'label_0'): return -1
    return 0

def evaluate_sentiment_pipe(pipe_fn, texts, true_labels):
    """Run a sentiment pipeline and return (preds, confidences, elapsed, accuracy, f1)."""
    preds, confs = [], []
    t0 = time.time()
    for text in texts:
        result = pipe_fn(text[:512])[0]
        preds.append(normalize_sentiment(result['label']))
        confs.append(result['score'])
    elapsed = time.time() - t0
    acc = accuracy_score(true_labels, preds)
    f1  = f1_score(true_labels, preds, average='macro', zero_division=0)
    return preds, confs, elapsed, acc, f1

