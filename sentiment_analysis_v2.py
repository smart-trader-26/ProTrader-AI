# CELL 7: Walk-Forward Validation + Stacking Ensemble
#   Walk-forward prevents look-ahead bias for time-series data.
#   Stacking: base models produce out-of-fold (OOF) probability
#   predictions, which a meta-model (LogReg) learns to combine.
#
#   NOTE: With tiny mock data these numbers are illustrative.
#   Run Cell 9 to collect real data, then re-run Cell 8 for
#   meaningful performance metrics.
