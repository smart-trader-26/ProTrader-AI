# ============================================================
# CELL 6: Feature Engineering
#   Three key improvements over the original:
#   1. Multi-category encoding: every category gets its own
#      sentiment column per day — no signal lost to mode-collapse.
#   2. Time-decay weighting: exponential decay on news recency.
#   3. Noise-filtered binary target: flat days removed.
# ============================================================

def build_daily_features(df_news_raw: pd.DataFrame,
                          trading_dates: pd.DatetimeIndex,
                          decay_lambda: float = DECAY_LAMBDA,
                          lookback_days: int   = NEWS_LOOKBACK_DAYS) -> pd.DataFrame:
    """
    For each trading date, builds a 12-column feature row:
      - Sentiment_{cat}: time-decay & confidence-weighted mean sentiment
        for all headlines in that category within the lookback window.
      - Count_{cat}: number of articles contributing to that category.

    Because every category gets its own column, a day with 3 Macro
    headlines AND 2 Earnings headlines produces meaningful values in
    BOTH columns — the original mode-collapse bug is eliminated.
    """
    rows = []
    df_news_raw = df_news_raw.copy()
    df_news_raw['date'] = pd.to_datetime(df_news_raw['date'])

    for tdate in trading_dates:
        window_start = tdate - pd.Timedelta(days=lookback_days)
        window = df_news_raw[
            (df_news_raw['date'] >= window_start) &
            (df_news_raw['date'] <= tdate)
        ].copy()

        row = {'Date': tdate}
        for cat in VALID_CATEGORIES:
            cat_df = window[window['category'] == cat].copy()
            if cat_df.empty:
                row[f"Sentiment_{cat}"] = 0.0
                row[f"Count_{cat}"]     = 0
                continue

            # Time decay: e^(-λ * days_ago)
            cat_df['days_ago']     = (tdate - cat_df['date']).dt.days
            cat_df['time_weight']  = np.exp(-decay_lambda * cat_df['days_ago'])
            cat_df['eff_weight']   = cat_df['sentiment_conf'] * cat_df['time_weight']

            total_w = cat_df['eff_weight'].sum()
            weighted_sent = (
                (cat_df['sentiment_val'] * cat_df['eff_weight']).sum() / total_w
                if total_w > 0 else 0.0
            )
            row[f"Sentiment_{cat}"] = weighted_sent
            row[f"Count_{cat}"]     = len(cat_df)

        rows.append(row)

    df_out = pd.DataFrame(rows).set_index('Date')
    df_out.index = pd.to_datetime(df_out.index).tz_localize(None)
    return df_out


def make_stock_target(ticker: str, start: str, end: str,
                      threshold: float = RETURN_THRESHOLD):
    """
    Download OHLCV, compute next-day return, apply noise filter.
    Returns df_stock with 'Target' column (1 = up, 0 = down).
    Flat days (|return| < threshold) are DROPPED — they are noise.
    """
    df = yf.download(ticker, start=start, end=end, progress=False)
    if isinstance(df.columns, pd.MultiIndex):
        df.columns = df.columns.get_level_values(0)
    df.index = pd.to_datetime(df.index).tz_localize(None)

    returns = df['Close'].pct_change().shift(-1)
    df['Return'] = returns
    df['Target'] = np.where(
        returns >  threshold, 1,
        np.where(returns < -threshold, 0, np.nan)
    )
    before = len(df)
    df = df.dropna(subset=['Target'])
    df['Target'] = df['Target'].astype(int)
    print(f"  Noise filter ({ticker}): kept {len(df)}/{before} days "
          f"(removed {before - len(df)} flat days ≤ ±{threshold*100:.1f}%)")
    return df


