# ── Helper: Article Body Extraction ───────────────────────
_REQ_HEADERS = {
    'User-Agent': (
        'Mozilla/5.0 (Windows NT 10.0; Win64; x64) '
        'AppleWebKit/537.36 (KHTML, like Gecko) '
        'Chrome/120.0.0.0 Safari/537.36'
    )
}

def fetch_article_body(url: str, char_limit: int = 1500) -> str | None:
    """
    Extract article body using trafilatura.
    Returns first `char_limit` chars, or None on failure.
    Falls back gracefully — headline analysis continues regardless.
    """
    if not url:
        return None
    try:
        resp = requests.get(url, headers=_REQ_HEADERS, timeout=8, verify=False)
        if resp.status_code == 200:
            body = trafilatura.extract(
                resp.text,
                include_comments=False,
                include_tables=False,
                favor_recall=True
            )
            if body and len(body) > 120:
                return body[:char_limit]
    except Exception:
        pass
    return None


