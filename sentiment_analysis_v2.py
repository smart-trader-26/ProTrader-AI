def create_feature_row(category: str, sentiment_val: float) -> pd.DataFrame:
    """Build 12-column feature vector — columns in exact training order."""
    row = {}
    for cat in VALID_CATEGORIES:
        row[f"Sentiment_{cat}"] = sentiment_val if cat == category else 0.0
    for cat in VALID_CATEGORIES:
        row[f"Count_{cat}"] = 1 if cat == category else 0
    return pd.DataFrame([row])[ALL_FEATURES].astype('float32')


def ensemble_predict(feature_row: pd.DataFrame):
    """
    Run all 4 base models, combine with stacker (or weighted average).
    Returns (probs dict, consensus float).
    """
    probs = {
        'LogReg':       ui_lr.predict_proba(feature_row)[0][1]  if ui_lr  else 0.5,
        'RandomForest': ui_rf.predict_proba(feature_row)[0][1]  if ui_rf  else 0.5,
        'XGBoost':      ui_xgb.predict_proba(feature_row)[0][1] if ui_xgb else 0.5,
        'LightGBM':     ui_lgb.predict_proba(feature_row)[0][1] if ui_lgb else 0.5,
    }
    if ui_stacker is not None:
        oof_input = np.array([[v for v in probs.values()]])
        consensus = ui_stacker.predict_proba(oof_input)[0][1]
    else:
        weights   = [abs(p - 0.5) * 2 + 0.1 for p in probs.values()]
        consensus = sum(p * w for p, w in zip(probs.values(), weights)) / sum(weights)
    return probs, consensus


