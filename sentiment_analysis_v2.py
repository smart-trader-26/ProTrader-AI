BULLISH_KEYWORDS = [
    "buy", "strong buy", "upgrade", "bullish", "surge", "rally", "soar",
    "record", "beat", "beats", "outperform", "positive", "growth", "win"
]
BEARISH_KEYWORDS = [
    "sell", "strong sell", "downgrade", "bearish", "drop", "crash",
    "plunge", "slump", "loss", "losses", "penalty", "fraud", "default",
    "miss", "misses", "decline", "warning", "cut", "disappoints"
]

def get_clean_sentiment(text: str):
    """
    Returns (sentiment_value: int, confidence: float)
    where sentiment_value ∈ {-1, 0, 1}.
    Applies domain keyword overrides for high-signal financial terms
    before falling back to the NLP model.
    """
    text_lower = text.lower()

    # Hard keyword overrides (high precision for clear financial signals)
    if any(w in text_lower for w in BEARISH_KEYWORDS):
        model_result = active_sentiment_pipe(text[:512])[0]
        model_label  = normalize_sentiment(model_result['label'])
        # Only override if model also agrees or is neutral (avoid false negatives)
        if model_label <= 0:
            return -1, max(model_result['score'], 0.88)

    if any(w in text_lower for w in BULLISH_KEYWORDS):
        model_result = active_sentiment_pipe(text[:512])[0]
        model_label  = normalize_sentiment(model_result['label'])
        if model_label >= 0:
            return 1, max(model_result['score'], 0.88)

    # Pure model output
    result = active_sentiment_pipe(text[:512])[0]
    return normalize_sentiment(result['label']), result['score']

