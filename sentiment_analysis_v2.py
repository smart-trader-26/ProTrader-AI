def translate_to_english(text: str, source_lang: str = 'hi'):
    """
    Translate text from regional language to English.
    Returns (translated_text, was_translated: bool).
    """
    try:
        time.sleep(0.4)  # Respect free-tier rate limits
        if source_lang == 'hi':
            result = _translator_hi.translate(text[:450])
        else:
            result = GoogleTranslator(source=source_lang, target='en').translate(text[:450])
        if result and result.strip() and result != text:
            return result.strip(), True
    except Exception:
        pass
    return text, False


# ── Helper: Robust GNews Fetch ────────────────────────────
def fetch_news_robust(company_name: str, start_d: datetime.date,
                      end_d: datetime.date, language: str = 'en',
                      max_retries: int = 3) -> list:
    """
    Fetch news with:
    - Exponential back-off retries
    - Freshness filter (reject articles older than 35 days)
    - Fallback query (shorter company name) if primary returns nothing
    """
    sd = (start_d.year, start_d.month, start_d.day)
    ed = (end_d.year,   end_d.month,   end_d.day)

    suffix_en = " stock NSE"
    suffix_hi = " शेयर बाजार"
    suffix    = suffix_hi if language == 'hi' else suffix_en

    queries = [
        urllib.parse.quote(company_name + suffix),
        urllib.parse.quote(company_name),             # Fallback: bare name
    ]

    max_days_old = 35

    for query in queries:
        for attempt in range(max_retries):
            try:
                time.sleep(1.5 + attempt * 2.0)
                gn = GNews(language=language, country='IN',
                           max_results=25, start_date=sd, end_date=ed)
                items = gn.get_news(query)

                if not items:
                    continue

                fresh = []
                for item in items:
                    try:
                        pub = pd.to_datetime(item['published date'])
                        if pub.tzinfo:
                            pub = pub.tz_localize(None)
                        # Sanity check: reject obviously wrong dates
                        if pub.year < 2020:
                            continue
                        if (datetime.datetime.now() - pub).days <= max_days_old:
                            fresh.append(item)
                    except Exception:
                        pass

                if fresh:
                    return fresh

            except Exception as e:
                wait = 5 * (attempt + 1)
                print(f"    ⚠️  Attempt {attempt+1} failed ({str(e)[:40]}). Retrying in {wait}s...")
                time.sleep(wait)

    return []  # All retries exhausted


