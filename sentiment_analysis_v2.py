from IPython.display import display, clear_output, HTML

def walk_forward_oof(models_dict: dict, X: pd.DataFrame,
                     y: pd.Series, n_splits: int = 5):
    """
    Time-series walk-forward cross-validation.
    Returns:
      fold_results_df : per-fold accuracy table
      oof_probs       : dict of model_name → full-length OOF probability array
    """
    # Auto-cap splits to avoid empty folds on small datasets
    n = max(2, min(n_splits, len(X) // 3))
    tscv = TimeSeriesSplit(n_splits=n)

    oof_probs    = {name: np.full(len(X), np.nan) for name in models_dict}
    fold_records = []

    for fold_i, (tr_idx, te_idx) in enumerate(tscv.split(X), start=1):
        # Skip folds where training data has only one class
        if len(np.unique(y.iloc[tr_idx])) < 2:
            print(f"  Fold {fold_i}: skipped (single class in training)")
            continue

        X_tr, X_te = X.iloc[tr_idx], X.iloc[te_idx]
        y_tr, y_te = y.iloc[tr_idx], y.iloc[te_idx]
        record = {'Fold': fold_i, 'Train': len(X_tr), 'Test': len(X_te)}

        for name, mdl in models_dict.items():
            mdl.fit(X_tr, y_tr)
            prob = mdl.predict_proba(X_te)[:, 1]
            oof_probs[name][te_idx] = prob
            record[f'{name}_Acc'] = accuracy_score(y_te, (prob > 0.5).astype(int))

        fold_records.append(record)

    return pd.DataFrame(fold_records), oof_probs


def build_stacking_meta(oof_probs: dict, y: pd.Series):
    """
    Train a LogisticRegression meta-model on OOF probability matrix.
    Returns trained meta-model (or None if insufficient data).
    """
    oof_matrix = np.column_stack([oof_probs[n] for n in oof_probs])
    valid      = ~np.isnan(oof_matrix).any(axis=1)

    if valid.sum() < 10:
        print("⚠️  Too few OOF samples for stacking. Run Cell 9 for real data.")
        return None

    X_meta = oof_matrix[valid]
    y_meta = y.values[valid]

    meta = LogisticRegression(random_state=42, max_iter=300, C=1.0)
    meta.fit(X_meta, y_meta)

    meta_preds = (meta.predict_proba(X_meta)[:, 1] > 0.5).astype(int)
    print(f"  Meta-model trained on {valid.sum()} OOF samples")
    print(f"  Meta-accuracy (in-sample): {accuracy_score(y_meta, meta_preds)*100:.1f}%")
    print(f"  Base model weights: { {n: round(w,3) for n,w in zip(oof_probs.keys(), meta.coef_[0])} }")
    return meta


